# inicialização
fim = 5        # número de iterações
cont = 0        # variável de controle

while cont < fim: # o loop irá ser feito *5* vezes
    print('Iteracao numero: ', cont) # faça alguma coisa, nesse caso, apenas imprima cont
    cont = cont + 1   # variável de controle precisa ser atualizada para garantir o fim do while